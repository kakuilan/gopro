//数组声明
package main
import "fmt"

func main(){
	a := [3]int{1,2,3} //声明长度为3的int数组
	b := [10]int{1,2,3} //声明长度为10的int数组,其中前3个元素初始化为1，2，3，其他默认为0
	c := [...]int{4,5,6} //可以省略长度而采用...的方式,GO会自动根据元素个数来计算长度

	fmt.Println(a,b,c)

	//声明一个二维数组,该数组以2个数组作为元素,其中每个数组又有4个int类型的元素
	doubleArr := [2][4]int{[4]int{1,2,3,4}, [4]int{5,6,7,8}}

	//如果内部的元素和外部的一样,那么上面的声明可以简化,直接忽略内部的类型
	easyArr := [2][4]int{{1,2,3,4}, {5,6,7,8}}
	fmt.Println(doubleArr, easyArr)
}
