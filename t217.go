// slice 切片
package main
import "fmt"

func main(){
    s1 := make([]int, 10)
    s2 := []int{1,2,3}

    fmt.Println(s1,s2)

    a := [...]int{1,2,3,4,5} //定义5个元素的数组
    s3 := a[2:4] //从序号2到3创建切片,包含元素3,4
    s4 := a[1:5] //从序号1到4创建切片,包含元素2,3,4,5
    s5 := a[:]   //用array中的所有元素创建切片,这是a[0:len(a)]的简化写法
    s6 := a[:4]  //从序号0到3创建切片,这是a[0:4]的简化写法,得到1,2,3,4
    s7 := s4[:]  //从s4创建切片,但仍然指向a

    fmt.Println(s3,s4,s5,s6,s7)

}
